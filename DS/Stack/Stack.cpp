/*
Stack Work using LIFO(Last In First Method)

// 🧠 Core Operations:
// push(x)   → Add element x to the top          [O(1)]
// pop()     → Remove and return top element     [O(1)]
// top()     → Return top element (no removal)   [O(1)]
// isEmpty() → Check if stack is empty           [O(1)]

    stack<int> st;
    st.push(10);    // Stack: 10
    st.push(20);    // Stack: 10, 20
    st.push(30);    // Stack: 10, 20, 30

🧱 Visual Example:
Top → | 30 |  ← top()
      | 20 |
      | 10 |
*/